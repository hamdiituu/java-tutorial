
public class Math {

    public static void main(String[] args) {
        System.out.println(java.lang.Math.min(1, 123));
        System.out.println(java.lang.Math.max(1, 123));
        System.out.println(java.lang.Math.sqrt(64));
        System.out.println(java.lang.Math.abs(-64));
        System.out.println((int) (java.lang.Math.random() * 100));
    }
}
